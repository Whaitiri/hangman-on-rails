    def generate_word
      wordList = []
      File.open("/usr/share/dict/words").each do |line|
        line = line.strip
        if line.strip.length > 7 or line.strip.length < 3
          next
        elsif line[0].match(/^[A-Z]/)
          next
        else
          wordList << line.strip
        end
      end
      return wordList[rand(0...wordList.count)]
    end
